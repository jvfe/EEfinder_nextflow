#!/usr/bin/env python

from Bio import SeqIO
import argparse

def get_length(input_file):
    with open(f"{input_file}.rn.fmt.lenght", "w") as output_length:
        length_list = []
        for seq_record in SeqIO.parse(input_file, "fasta"):
            output_length.write(f"{seq_record.id}\t{str(len(seq_record))}\n")

parser = argparse.ArgumentParser(description="Creates a length file with the module SeqIO.")
parser.add_argument("--input_file", type=str, help="formated genome, generated by cut_seq function")
args = parser.parse_args()
input_file = args.input_file

get_length(input_file)